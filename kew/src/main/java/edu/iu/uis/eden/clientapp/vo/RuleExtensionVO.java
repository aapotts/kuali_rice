package edu.iu.uis.eden.clientapp.vo;

/**
 * @workflow.webservice-object
 */
public class RuleExtensionVO implements java.io.Serializable {

    private static final long serialVersionUID = 4154136053584801446L;

    protected String key;
    protected String value;

    public RuleExtensionVO() {}

    public RuleExtensionVO(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getKey() {
        return key;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }
}
